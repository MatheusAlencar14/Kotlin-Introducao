import kotlin.math.*

fun main() {

    //Verificar o maior entre dois números
    println(max(10, 17))
    //Verificar o menor entre dois números
    println(min(10, 17))
    //Verificar raiz quadrada
    println(sqrt(25f))
    //Numero de PI
    println(PI)
    //Numero de Euler
    println(E)
    //Arredondar o valor de um número
    println(round(8.54))

}